package lesson7;

public class Calculator {

    public double sum(double n1, double n2) {
        return n1 + n2;
    }

    public double multiply(double n1, double n2) {
        return n1 * n2;
    }

    public static double staticSum(double n1, double n2) {
        return n1 + n2;
    }

    public static double staticMultiply(double n1, double n2) {
        return n1 * n2;
    }
}
